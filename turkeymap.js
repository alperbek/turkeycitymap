function fireTurkeyMapUpdated(selected){
    for(var i=0;i<turkeyMapListeners.length;i++){
        turkeyMapListeners[i](selected);
    }
}


function renderTurkey(){
   // $("#map").css("cursor","pointer");
    var lineFunction = d3.svg.line()
        .x(function(d) { return d[0]; })
        .y(function(d) { return d[1]; })
        .interpolate("linear");


    svgContainer = d3.select("#map").append("svg")
        .attr("width", width)
        .attr("height", height);


    //Background Layout
    var bg=svgContainer.append("rect")
        .attr("class", "background")
        .attr("width", width)
        .attr("height", height)
        .attr("fill", "none")
        .attr("pointer-events", "all")
        .on("click", click);


    cityGraphics = svgContainer.append("g").attr("id", "states");



    for(var i=0;i<cityMap.length;i++){
        cityGraphics.append("path").attr("d", lineFunction(cityMap[i]))
            .attr("stroke", "#FFFFFF")
            .attr("stroke-width", 0.5)
            .attr("fill", "#D1EDF6")
            .on("click", click);
    }
    cityGraphics.selectAll("path").data(cityNameMap);

}





function click() {

    //d3.selectAll("path").style("fill","D1EDF6");

    var d=this;
    var x, y, k;

    var bgClickFlag=d3.select(this).attr("class")==='background';
    if (d && centered !== d && !bgClickFlag) {
        var centroid = getCentroid(d3.select(this));
        x = centroid[0];
        y = centroid[1];
        k = 4;
        centered = d;
    } else {
        x = width / 2;
        y = height / 2;
        k = 1;
        centered = null;
    }

    cityGraphics.transition()
        .duration(1000)
        .attr("transform", "translate(" + width / 2 + "," + height / 2 + ")scale(" + k + ")translate(" + -x + "," + -y + ")")
        .style("stroke-width", 1.5 / k + "px");


    fireTurkeyMapUpdated(centered);

}

function getCentroid(selection) {
    var element = selection.node(),
        bbox = element.getBBox();
    return [bbox.x + bbox.width/2, bbox.y + bbox.height/2];
}



